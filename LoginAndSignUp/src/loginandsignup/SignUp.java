    private void SignUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBtnActionPerformed
        // TODO add your handling code here:
        // System.out.println("Sign up btn clicked");
        String fullName, email, Password, phone_number, address, query;
        String SUrl, SUser, SPass;
        SUrl = "jdbc:MySQL://localhost:3306/java_user_database";
        SUser = "root"; //username of my sql
        SPass = ""; //psw if my sql
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();
            if("".equals(fname.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Full Name is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(emailAddress.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Email Address is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(pass.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Password is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
             }else if("".equals(tel.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Mobile Number is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else if("".equals(address1.getText())){
                JOptionPane.showMessageDialog(new JFrame(), "Address is require", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }else {
            fullName = fname.getText(); 
            email    = emailAddress.getText();
            Password = pass.getText();
            System.out.println(Password);
            phone_number = tel.getText();
            address = address1.getText();
            
            query = "INSERT INTO user(full_name, phone_number, email, password, Address)"+
                    "VALUES('"+fullName+"', '"+phone_number+"' , '"+email+"' , '"+Password+"' , '"+address+"')";
            
            st.execute(query);
            fname.setText("");
            tel.setText("");
            emailAddress.setText("");
            pass.setText("");
            address1.setText("");

            showMessageDialog(null, "New account has been created successfully!");
            }
        }catch(Exception e){
           System.out.println("Error!" + e.getMessage()); 
        }
        
    }//GEN-LAST:event_SignUpBtnActionPerformed

